*                                                                      *
      subroutine dlsnacc(mass,pt,y,iacc)
c.
c.    this routine makes the filter on the dls acceptance.
c.    inputs are: mass in gev, pt in gev/c and y (lab),
c.    (mass, pt  and y being those of the dilepton).
c.    it returns iacc=1 if point falls within the dls acceptance
c.                    0 if not
*                                                                      *
************************************************************************
      implicit none
      real*8 mass,pt,y,acc,accntest,eac
      integer iacc
*-----------------------------------------------------------------------
*
      call getnacc(mass,pt,y,acc,eac)
      iacc=0
      if(acc.le.0.)go to 999
      accntest=eac/acc
      if(accntest.gt.0.5)go to 999
      iacc=1
c
  999 continue
      return
      end

c
c
c
      subroutine getnacc(mass,pt,y,acc,eac)
c.    ******************
c.
c.         this routine makes interpolation and returns acceptance
c.
      external alsnacc,alsneac
      real*8 mass,mmin,pt,y,acc,eac
      data mmin,ptmin,ymin/0.05,0.,0.5/
      data dm,dp,dy/.05,.1,.2/
c
      im=mass/dm
      ip=pt/dp+1.
      iy=y/dy-1.5
      if((im.lt.1).or.(im.gt.24))then
           acc=0.
           go to 999
           end if
      if((ip.lt.1).or.(ip.gt.8))then
           acc=0.
           go to 999
           end if
      if((iy.lt.1).or.(iy.gt.7))then
           acc=0.
           go to 999
           end if
c
      u=mass
      v=pt
      t=y
c
      cm=mmin-dm/2.+float(im)*dm
      cp=ptmin-dp/2.+float(ip)*dp
      cy=ymin-dy/2.+float(iy)*dy
      if(im.eq.1)then
              u0=cm
              go to 100
                 end if
      if(im.eq.24)then
              u0=cm-dm
              go to 100
                 end if
      u0=cm
      if((mass-cm).lt.0.)u0=cm-dm
  100 u1=u0+dm
      if(ip.eq.1)then
              v0=cp
              go to 101
                 end if
      if(ip.eq.8)then
              v0=cp-dp
              go to 101
                 end if
      v0=cp
      if((pt-cp).lt.0.)v0=cp-dp
  101 v1=v0+dp
      if(iy.eq.1)then
              t0=cy
              go to 102
                 end if
      if(iy.eq.7)then
              t0=cy-dy
              go to 102
                 end if
      t0=cy
      if((y-cy).lt.0.)t0=cy-dy
  102 t1=t0+dy
c
      x0=alsnacc(u0,v0,t0)
      x1=alsnacc(u1,v0,t0)
      x2=alsnacc(u0,v1,t0)
      x3=alsnacc(u0,v0,t1)
      x4=alsnacc(u1,v1,t0)
      x5=alsnacc(u1,v0,t1)
      x6=alsnacc(u0,v1,t1)
      x7=alsnacc(u1,v1,t1)
c
      e0=alsneac(u0,v0,t0)
      e0=e0*e0
      e1=alsneac(u1,v0,t0)
      e1=e1*e1
      e2=alsneac(u0,v1,t0)
      e2=e2*e2
      e3=alsneac(u0,v0,t1)
      e3=e3*e3
      e4=alsneac(u1,v1,t0)
      e4=e4*e4
      e5=alsneac(u1,v0,t1)
      e5=e5*e5
      e6=alsneac(u0,v1,t1)
      e6=e6*e6
      e7=alsneac(u1,v1,t1)
      e7=e7*e7
c
      a=(u-u0)/dm
      b=(v-v0)/dp
      c=(t-t0)/dy
c
      p0=1.-a-b-c+a*b+a*c+b*c-a*b*c
      p1=a*(1.-b-c+b*c)
      p2=b*(1.-a-c+a*c)
      p3=c*(1.-a-b+a*b)
      p4=a*b*(1.-c)
      p5=a*c*(1.-b)
      p6=b*c*(1.-a)
      p7=a*b*c
c
      acc=x0*p0+x1*p1+x2*p2+x3*p3+x4*p4+x5*p5+x6*p6+x7*p7
      eac=e0*p0**2+e1*p1**2+e2*p2**2+e3*p3**2+e4*p4**2+
     +    e5*p5**2+e6*p6**2+e7*p7**2
      eac=sqrt(eac)
c
  999 return
      end
